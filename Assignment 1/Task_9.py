# Write a program to find the sum of numbers from 1 to 100 using a loop.


sum = 0
for i in range(1, 101):  # start loop  from 1 to 100
    sum += i  # initially sum will be 1 in first step then loop will continue up to 100

print("Sum of the numnber is : ", sum)  # reason that it will just print the sum of all

